package cn.com.antika.bean;

public class ImageContent {
	private String  imageContentByte;
	private String  imageFormat;
	private int     imageType;
	public String getImageContentByte() {
		return imageContentByte;
	}
	public String getImageFormat() {
		return imageFormat;
	}
	public void setImageContentByte(String imageContentByte) {
		this.imageContentByte = imageContentByte;
	}
	public void setImageFormat(String imageFormat) {
		this.imageFormat = imageFormat;
	}
	public int getImageType() {
		return imageType;
	}
	public void setImageType(int imageType) {
		this.imageType = imageType;
	}
}	
